    private void piButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_piButtonActionPerformed
        // TODO add your handling code here:
        
        
        
        if(this.ntrials==0){
            JOptionPane.showMessageDialog(this, "Please enter number of trials!", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
      
        progressBar.setMinimum(0);
        progressBar.setMaximum(this.ntrials);
        progressBar.setValue(0);
        
        
        Graphics2D plt = (Graphics2D)mainPanel.getGraphics();
        
        setupCircle(plt);
        
        System.out.println("Computing Pi");
        
        Random rand = new Random();
        
        int in = 0;
        
         progressBar.setIndeterminate(true);
        
        for(int i=0;i<this.ntrials;i++){
           //System.out.print(Double.toString(rand.nextDouble())+'\n');
           
          double r1 = rand.nextDouble();
          double r2 =  rand.nextDouble();
          
          int rx = (int)(r1*this.mainPanel.getWidth()/2 + this.mainPanel.getWidth()/2);
          int ry = (int)(r2*this.mainPanel.getHeight()/2 + this.mainPanel.getHeight()/2);
          
         // int xint = rx - this.mainPanel.getWidth()/2;
         //int yint =this.mainPanel.getHeight()/2 - ry;
          
 
          double radius = Math.sqrt(r1*r1 + r2*r2);
        
          if(radius < 1.0)
          {
          plt.fillOval(rx, ry, 5, 5);
          in++;
          }
          
           
        }
        
        progressBar.setIndeterminate(false);
        progressBar.setValue(this.ntrials);
        
        pi = ((double)in/(double)this.ntrials)*4.0;
        
             
        outputArea.append("\nPi: \n"+pi);
        
    }//GEN-LAST:event_piButtonActionPerformed

