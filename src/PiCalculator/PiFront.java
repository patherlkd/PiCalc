    private void setupCircle(Graphics2D g)
    {
        int dia = mainPanel.getWidth();
        
        int center = mainPanel.getWidth()/2;
       
       g.setColor(Color.WHITE);
      g.fillRect(center - dia/2,center - dia/2, dia, dia);
        
       g.setColor(Color.BLACK);
       
        g.drawOval(center - dia/2,center - dia/2, dia, dia);
        g.drawLine(0, center, center*2, center);
        g.drawLine(center, 0, center, center*2);
        
    }
